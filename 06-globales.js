// Javascript tiene objetos globales como por ejemplo global, window, document, Date, etc.

// parseo de numeros

var entero = parseInt('0xff002', 8); //https://developer.mozilla.org/es/docs/Web/JavaScript/Referencia/Objetos_globales/parseInt
